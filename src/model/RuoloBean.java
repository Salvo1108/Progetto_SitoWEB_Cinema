
package model;

public class RuoloBean {
	private int idRuolo;
	private String ruolo;

	public RuoloBean() {
		idRuolo = 0;
		ruolo = null;
	}

	public int getIdRuolo() {
		return idRuolo;
	}

	public void setIdRuolo(int idRuolo) {
		this.idRuolo = idRuolo;
	}

	public String getRuolo() {
		return ruolo;
	}

	public void setRuolo(String ruolo) {
		this.ruolo = ruolo;
	}
}
